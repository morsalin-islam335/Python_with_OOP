# Create a list of dictionaries
list_of_dicts = [
    {"name": "Alice", "age": 30},
    {"name": "Bob", "age": 25},
    {"name": "Charlie", "age": 35}
]

# Access the first dictionary in the list by index (0)
first_dict = list_of_dicts[0]

# Access values inside the first dictionary using keys
name_of_first_person = first_dict["name"]
age_of_first_person = first_dict["age"]

# Print the values
print(f"Name: {name_of_first_person}, Age: {age_of_first_person}")

